    private void inboxListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inboxListMouseClicked
         if(evt.getClickCount() == 2)
         {
             int index = inboxList.locationToIndex(evt.getPoint());
             if (index >= 0) {
                EMail email = (EMail)inboxList.getModel().getElementAt(index);
                con.writeMail(index);
                bodyText.setText(con.getBox().getMessage(index).getMessageBody());
          }
         }
    }//GEN-LAST:event_inboxListMouseClicked

