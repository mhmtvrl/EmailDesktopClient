    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {
        String host = hostTextField.getText();
        int port = Integer.parseInt(portTextField.getText());
        String userName = userNameTextField.getText();
        String password = passwordTextField.getText();
        Session session = new Session(host, port, userName, password );
        con = new Connector(session);
        if(con.connect())
        {
            logLable.setText("connected");
            if(con.login())
            {
                logLable.setText("Connected and logged in");
                con.selectInbox();
                con.readInbox();
                model = new DefaultListModel();
                inboxList.setModel(model);
                MessageBox box = con.getBox();
                for(int i = 0; i < box.getSize(); i++)
                {
                    model.addElement(box.getMessage(i));
                }
                
                
            }
            else
                logLable.setText("Connected but cannot logged in");
        }
        else
        {
            logLable.setText("connection failed");
        }
    }//GEN-LAST:event_loginButtonActionPerformed

